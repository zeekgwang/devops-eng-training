def add_func(x: str, y: str) -> int:
    return int(x) + int(y)


def subtract_func(x: str, y: str) -> int:
    return int(x) - int(y)


def multiply_func(x: str, y: str) -> int:
    return int(x) * int(y)


def division_func(x: str, y: str) -> float:
    return int(x) / int(y)